/*
 * Escreva um algoritmo que exiba 20 vezes a mensagem “Eu gosto de estudar Algoritmos!”. Utilize o 
 * comando de repetição Para (For)
 */

package alba.comandos_repeticao;

public class E26 {
    public static void main(String[] args) {
        for (int i = 0; i < 20; i++) {
            System.out.println("\nEu gosto de estudar Algoritmos!\n");
        }
    }
}
